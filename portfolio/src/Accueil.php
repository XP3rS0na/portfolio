        <h4><b>Auto-GPT</b></h4>
            <p>
                <b>Auto-GPT</b> est un projet open source disponible sur Github (<i><a class="srcs" href="https://github.com/Significant-Gravitas/AutoGPT">ici</a></i>) 
                développé en Python et utilisant l'API de GPT-4 pour développer des projets de manière autonome en créant des agents (paramétrables)
                 qui vont prendre des décisions sans intervention humaine.<br><br>
                 Exemple : un utilisateur utilise <b>Auto-GPT</b> pour la planification de l'anniversaire de son fils de 5 ans. 
                 <b>Auto-GPT</b> va alors faire des recherches sur internet (fonctionnalité absente de ChatGPT) générer une liste de tâches pour le bon déroulement de l'anniversaire, 
                 et réaliser un compte-rendu comprenant thèmes de la fête, lieu, nourriture, idées de cadeau, etc... Pour chacune des tâches et les faisant valider une par une, <b>Auto-GPT</b> va créer un agent qui effectuera toutes les démarches, 
                 choisissant un thème, réservant un lieu d'accueil de la fête, réalisant des recettes de cuisine ou commandant un traiteur, et pourra même acheter un ligne des cadeaux, <b>sans intervention d'opérateur tiers</b>.
                 <br>Il s'agit donc d'un premier aperçu de ce qu'on peut appeler une Intelligence Artificielle Générale.
