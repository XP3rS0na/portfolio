        <h2>Veille technologique</h2>
        <h3>Qu'est-ce que la veille technologique ?</h3>
        <p>La veille technologique, élément de la veille stratégique, consiste à surveiller les évolutions techniques, 
        les innovations dans un secteur d’activité donnée. La veille technologique comprend notamment la surveillance, la collecte, 
        le partage et la diffusion d’information permettant d’anticiper ou de s’informer sur des changements en matière de recherche, 
        développement, brevet, lancement de nouveaux produits, matériaux, processus, concepts, innovation de fabrication, etc…. 
        Le but de la veille technologique est de rester informé des avancées et des évolutions dans le domaine de la technologie, permettant aux entreprises et aux professionnels de suivre les dernières tendances, 
        de rester compétitifs sur le marché et de prendre des décisions éclairées concernant l'adoption de nouvelles technologies...<p>
        
        <h3>Quels sont les outils de ma veille ?</h3>
        <p>J'ai effectué ma veille avec différents outils tels que :
            <ul>
                <li><b>Feedly</b>, un agrégateur de flux RSS sur lequel on peut cibler des mots clés et lire des centaines d'articles chaque semaine sur un sujet choisi.</li>
                <li><b>Underscore_</b>, un podcast tech.</li>
                <li><b>Openai.com</b>, le site web du célèbre Chat-GPT, et les articles de recherches de son équipe de R & D.</li>
                <li><b>Les journaux traditionnels ou web</b>, tels que Le Monde, The Guardian, JDN.</li>
            </ul>
        </p>
        <h3>Quel est le sujet de ma veille technologique ?</h3>
        <p>J'ai réalisé cette veille sur le <b>Large Language Model <i>GPT-4</i></b>, devenus très populaire fin 2022 dans sa version 3.5 et n'apportant pas moins de révolution dans notre vie que de questions sur notre avenir.</p>
        <h3>Définition d'un Large Language Model</h3>
        <p>Les LLM sont un segment de l'intelligence artificielle qui excelle dans l'imitation de la conversation naturelle d'êtres humains. Basés sur de vastes réseaux neuronaux et entrainés sur d'énormes volumes de données (570 Go de texte brut pour GPT-3)
        <br>Différents types de LLM se distinguent : 
            <ul>
                <li><b>Les GPT :</b> pour Generative Pre-trained Transformer, sont des modèles de langage particulièrement efficaces dans la génération de contenu, la traduction et l'interaction avec des chatbots</li>
                <li><b>Les BERT :</b> pour Bidirectional Encoder Representations from Transformers, analysent le contexte d'un texte de gauche à droite, et de droite à gauche, permettant d'appréhender avec précision le contexte émotionnel d'un texte par exemple.</li>
                <li><b>XLnet</b> est un modèle de langage s'appelant et se réencodant pour "deviner" le mot suivant, permutant ses paramètres au fur et à mesure de l'analyse.</li>
                <li><b>T5 :</b> pour Text-to-Text Transfer Transformer, spécialisés dans la réponse de texte à un texte, ces modèles sont particulièrement utilisés pour résumer des textes ou générer des prompts type question-réponses basés sur leur support de travail.</li>
            </ul><br><i>Sources : <a class="srcs" href="https://medium.com/@letscodeai/unveiling-the-potential-of-large-language-models-types-advantages-and-versatile-applications-3440f6a2d281">ce lien</a></i></p>
